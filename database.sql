CREATE DATABASE IF NOT EXISTS atendeez_db;
USE atendeez_db;

-- 1. Admins Table
CREATE TABLE IF NOT EXISTS Admins (
    admin_id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL
);

-- 2. Teachers Table
CREATE TABLE IF NOT EXISTS Teachers (
    uTID VARCHAR(20) PRIMARY KEY,
    first_name VARCHAR(50) NOT NULL,
    middle_name VARCHAR(50),
    last_name VARCHAR(50) NOT NULL,
    department VARCHAR(100),
    email VARCHAR(100) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    status ENUM('Active', 'Archived') DEFAULT 'Active',
    failed_attempts INT DEFAULT 0,
    lockout_time DATETIME NULL
);

-- 3. Students Table
CREATE TABLE IF NOT EXISTS Students (
    uSID VARCHAR(20) PRIMARY KEY,
    first_name VARCHAR(50) NOT NULL,
    middle_name VARCHAR(50),
    last_name VARCHAR(50) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    course VARCHAR(100),
    level VARCHAR(20),
    section VARCHAR(50),
    status ENUM('Active', 'Archived') DEFAULT 'Active',
    failed_attempts INT DEFAULT 0,
    lockout_time DATETIME NULL
);

-- 4. Subjects Table
CREATE TABLE IF NOT EXISTS Subjects (
    subject_id INT AUTO_INCREMENT PRIMARY KEY,
    subject_code VARCHAR(20) UNIQUE NOT NULL,
    subject_name VARCHAR(100) NOT NULL
);

-- 5. Teacher Assignments Table
CREATE TABLE IF NOT EXISTS Teacher_Assignments (
    assignment_id INT AUTO_INCREMENT PRIMARY KEY,
    uTID VARCHAR(20),
    subject_id INT,
    section VARCHAR(50),
    FOREIGN KEY (uTID) REFERENCES Teachers(uTID) ON DELETE CASCADE,
    FOREIGN KEY (subject_id) REFERENCES Subjects(subject_id) ON DELETE CASCADE
);

-- 6. Enrollments Table (Students enrolled in subjects/sections)
CREATE TABLE IF NOT EXISTS Enrollments (
    enrollment_id INT AUTO_INCREMENT PRIMARY KEY,
    uSID VARCHAR(20),
    subject_id INT,
    section VARCHAR(50),
    FOREIGN KEY (uSID) REFERENCES Students(uSID) ON DELETE CASCADE,
    FOREIGN KEY (subject_id) REFERENCES Subjects(subject_id) ON DELETE CASCADE
);

-- 7. Schedule Table (Class timings)
CREATE TABLE IF NOT EXISTS schedule (
    schedule_id INT AUTO_INCREMENT PRIMARY KEY,
    subject_id INT,
    uTID VARCHAR(20),
    section VARCHAR(50),
    day_of_week VARCHAR(20),
    start_time TIME,
    end_time TIME,
    room VARCHAR(50),
    FOREIGN KEY (subject_id) REFERENCES Subjects(subject_id) ON DELETE CASCADE,
    FOREIGN KEY (uTID) REFERENCES Teachers(uTID) ON DELETE CASCADE
);

-- 7. Sessions Table (QR generated by teachers)
CREATE TABLE IF NOT EXISTS Sessions (
    session_id VARCHAR(50) PRIMARY KEY,
    uTID VARCHAR(20),
    subject_id INT,
    section VARCHAR(50),
    random_token VARCHAR(50) NOT NULL,
    start_time DATETIME NOT NULL,
    expires_at DATETIME NOT NULL,
    latitude DECIMAL(10, 8),
    longitude DECIMAL(11, 8),
    status ENUM('Active', 'Ended') DEFAULT 'Active',
    FOREIGN KEY (uTID) REFERENCES Teachers(uTID),
    FOREIGN KEY (subject_id) REFERENCES Subjects(subject_id)
);

-- 8. Attendance Table
CREATE TABLE IF NOT EXISTS Attendance (
    attendance_id INT AUTO_INCREMENT PRIMARY KEY,
    session_id VARCHAR(50),
    uSID VARCHAR(20),
    scan_time DATETIME NOT NULL,
    status ENUM('Present', 'Absent', 'Late') DEFAULT 'Present',
    remarks VARCHAR(255),
    is_valid ENUM('Valid', 'Invalid') DEFAULT 'Valid', -- For tab switching/suspicious activity
    FOREIGN KEY (session_id) REFERENCES Sessions(session_id),
    FOREIGN KEY (uSID) REFERENCES Students(uSID),
    UNIQUE KEY unique_scan (session_id, uSID) -- Prevent duplicate scans in same session
);

-- 9. Notifications Table
CREATE TABLE IF NOT EXISTS Notifications (
    notification_id INT AUTO_INCREMENT PRIMARY KEY,
    uSID VARCHAR(20),
    message VARCHAR(255) NOT NULL,
    type ENUM('Info', 'Warning', 'Alert') DEFAULT 'Info',
    is_read BOOLEAN DEFAULT FALSE,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (uSID) REFERENCES Students(uSID) ON DELETE CASCADE
);

-- 10. Submitted Reports Table
CREATE TABLE IF NOT EXISTS Submitted_Reports (
    report_id INT AUTO_INCREMENT PRIMARY KEY,
    uTID VARCHAR(20),
    subject_id INT,
    section VARCHAR(50),
    submission_date DATETIME DEFAULT CURRENT_TIMESTAMP,
    summary_json LONGTEXT,
    status ENUM('Submitted', 'Reviewed') DEFAULT 'Submitted',
    FOREIGN KEY (uTID) REFERENCES Teachers(uTID),
    FOREIGN KEY (subject_id) REFERENCES Subjects(subject_id)
);

-- 11. Drop Requests Table (Teacher requests to archive student)
CREATE TABLE IF NOT EXISTS Drop_Requests (
    request_id INT AUTO_INCREMENT PRIMARY KEY,
    uTID VARCHAR(20),
    uSID VARCHAR(20),
    subject_id INT,
    reason TEXT,
    status ENUM('Pending', 'Approved', 'Rejected') DEFAULT 'Pending',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (uTID) REFERENCES Teachers(uTID),
    FOREIGN KEY (uSID) REFERENCES Students(uSID),
    FOREIGN KEY (subject_id) REFERENCES Subjects(subject_id)
);

-- 12. Excuse Letters Table
CREATE TABLE IF NOT EXISTS Excuse_Letters (
    letter_id INT AUTO_INCREMENT PRIMARY KEY,
    uSID VARCHAR(20),
    uTID VARCHAR(20),
    subject_id INT,
    message TEXT NOT NULL,
    file_path VARCHAR(255),
    status ENUM('Pending', 'Approved', 'Rejected') DEFAULT 'Pending',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (uSID) REFERENCES Students(uSID),
    FOREIGN KEY (uTID) REFERENCES Teachers(uTID),
    FOREIGN KEY (subject_id) REFERENCES Subjects(subject_id)
);

