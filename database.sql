-- Stored Procedure: Record Attendance with GPS check and Validity
DELIMITER //
CREATE PROCEDURE sp_RecordAttendance(
    IN p_student_id VARCHAR(20),
    IN p_session_id VARCHAR(50),
    IN p_token VARCHAR(50),
    IN p_student_lat DECIMAL(10,8),
    IN p_student_lon DECIMAL(11,8),
    IN p_is_valid ENUM('Valid', 'Invalid'),
    OUT p_result VARCHAR(255)
)
BEGIN
    DECLARE v_teacher_lat DECIMAL(10,8);
    DECLARE v_teacher_lon DECIMAL(11,8);
    DECLARE v_expires_at DATETIME;
    DECLARE v_session_token VARCHAR(50);
    DECLARE v_status ENUM('Active', 'Ended');
    DECLARE v_distance DECIMAL(10,2);
    DECLARE v_exists INT;
    
    -- Check if student is already marked for this session
    SELECT COUNT(*) INTO v_exists FROM Attendance WHERE session_id = p_session_id AND uSID = p_student_id;
    
    IF v_exists > 0 THEN
        SET p_result = 'Already scanned for this session.';
    ELSE
        -- Get Session details
        SELECT latitude, longitude, expires_at, random_token, status 
        INTO v_teacher_lat, v_teacher_lon, v_expires_at, v_session_token, v_status
        FROM Sessions WHERE session_id = p_session_id;
        
        IF v_status = 'Ended' THEN
            SET p_result = 'Session has already ended.';
        ELSEIF NOW() > v_expires_at THEN
            SET p_result = 'QR Code has expired.';
        ELSEIF p_token != v_session_token THEN
            SET p_result = 'Invalid QR Token.';
        ELSE
            -- Calculate distance in meters using Haversine formula (Earth radius ~ 6371km)
            SET v_distance = 6371000 * ACOS(
                COS(RADIANS(v_teacher_lat)) * COS(RADIANS(p_student_lat)) *
                COS(RADIANS(p_student_lon) - RADIANS(v_teacher_lon)) +
                SIN(RADIANS(v_teacher_lat)) * SIN(RADIANS(p_student_lat))
            );
            
            IF v_distance > 50 THEN
                SET p_result = CONCAT('Out of range. Distance: ', ROUND(v_distance, 0), ' meters. Allowed: 50m.');
            ELSE
                -- Insert Attendance
                INSERT INTO Attendance (session_id, uSID, scan_time, status, is_valid)
                VALUES (p_session_id, p_student_id, NOW(), 'Present', p_is_valid);
                
                -- Insert Notification
                INSERT INTO Notifications (uSID, message, type)
                VALUES (p_student_id, CONCAT('Attendance marked Present for session: ', p_session_id), 'Info');
                
                IF p_is_valid = 'Invalid' THEN
                    SET p_result = 'Success but flagged for suspicious activity.';
                ELSE
                    SET p_result = 'Attendance successfully recorded.';
                END IF;
            END IF;
        END IF;
    END IF;
END //
DELIMITER ;
